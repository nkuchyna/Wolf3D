#include "libft.h"

void	ft_putstr(char const *s)
{
	int		i;
	char	*str;

	if (!s)
		return ;
	i = 0;
	str = (char*)s;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}
