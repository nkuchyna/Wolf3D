#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		k;
	char	*c;

	c = s1;
	i = ft_strlen(s1);
	k = 0;
	while (s2[k] != '\0')
		s1[i++] = s2[k++];
	s1[i] = '\0';
	return (c);
}
