#include "libft.h"

static void		ft_check_putnbr(int n, int *d)
{
	if ((n > -2147483648 && n < -999999999) ||
		(n <= 2147483647 && n > 999999999))
		(*d) = 1000000000;
	else
	{
		while (n / (*d) != 0)
			(*d) *= 10;
		if (n != 0)
			(*d) = (*d) / 10;
	}
}

void			ft_putnbr(int n)
{
	int		d;
	char	c;

	d = 1;
	if (n == -2147483648)
	{
		write(1, "-2", 2);
		n = 147483648;
	}
	ft_check_putnbr(n, &d);
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	while (d > 0)
	{
		c = n / d + '0';
		ft_putchar(c);
		n = n - (n / d) * d;
		d = d / 10;
	}
}
